package tugas6_233040125;

public class Produk {
	public String nama;
	public double harga;
   
	public Produk(String nama, double harga) {
		this.nama = nama;
	    this.harga = harga;
	}

	public String getNama() {
		return nama;
	}

	public double getHarga() {
		return harga;
	}

}
